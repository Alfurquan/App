package com.example.App.models;

public class ShapeInput {
    private String type;
    private int radius;
    private int length;
    private int breadth;
    private int side;

    public String getType() {
        return type;
    }

    public int getRadius() {
        return radius;
    }

    public int getLength() {
        return length;
    }

    public int getBreadth() {
        return breadth;
    }

    public int getSide() {
        return side;
    }
}
